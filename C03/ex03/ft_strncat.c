char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (dest[i])
	{
		i++;
	}
	while (j < nb && src[j])
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}
#include  <stdio.h>
int main()
{

	char arr[]  = "ali";
	char arr1[] = "mehmet";

	printf("%s", ft_strncat(arr, arr1, 3));
}